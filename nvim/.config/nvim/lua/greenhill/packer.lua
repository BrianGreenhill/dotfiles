			-- Snippets
			{ "L3MON4D3/LuaSnip" },
			{ "rafamadriz/friendly-snippets" },
		},
	})

	-- tpope
	use("tpope/vim-commentary")
	use("tpope/vim-fugitive")

	-- bootstrap packer
	if packer_bootstrap then
		require("packer").sync()
	end
end)
